    private void saveNetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveNetActionPerformed
        // TODO add your handling code here:
       
         String name = adminNametxt.getText();
        String password = String.valueOf(adminPasstxt.getPassword());
        String username = adminUsernametxt.getText();
        if(name.equalsIgnoreCase("")||password.equalsIgnoreCase("")||username.equalsIgnoreCase(""))
        {
            JOptionPane.showMessageDialog(null, "Please enter all the details.");
        }
        else
        {
           useraccount.getEmployee().setName(name);
           useraccount.setUserID(username);
           useraccount.setUserPWD(password);
           
          
            JOptionPane.showMessageDialog(null, "Admin Details updated");
            adminNametxt.setText("");
adminPasstxt.setText("");
        adminUsernametxt.setText("");
        }

    }//GEN-LAST:event_saveNetActionPerformed


